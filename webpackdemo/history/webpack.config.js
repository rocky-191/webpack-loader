//webpack是node写法
let path=require('path');
let HtmlWebpackPlugin=require('html-webpack-plugin');
module.exports={
    devServer:{//开发服务器的配置
        port:3000,
        progress:true,
        contentBase:'./build',
        compress:true,
    },
    mode:'development',//模式分两种development,production
    entry:'./src/index.js',//入口文件
    output:{
        filename:'bundle.[hash].js',//打包后的文件名
        path:path.resolve(__dirname,'build'),//路径必须是一个绝对路径
    },
    plugins:[//存放webpack插件
        new HtmlWebpackPlugin({
            template:'./src/index.html',
            filename:'index.html',
            minify:{
                removeAttributeQuotes:true,
                collapseWhitespace:true
            },
            hash:true
        })
    ],
    module:{
        rules:[
            //loader顺序从右往左执行
            {
                test:/\.css$/,
                use:[
                    {
                        loader:'style-loader',
                        options:{
                            insertAt:'top'
                        }
                    },
                    'css-loader'
                ]
            },
            {
                test:/\.less$/,
                use:[
                    {
                        loader:'style-loader',
                        options:{
                            insertAt:'top'
                        }
                    },
                    'css-loader',
                    'less-loader'
                ]
            },
        ]
    }
}